// Task 100: Finding square root
function findSquareRoot(num) {
    return Math.sqrt(num); // We use Math.sqrt() function to find a square root of a number
}
console.log(findSquareRoot(144)); // Printing the value and it will give the output 12
// Task 101: Generating a random integar
function generateRandomNumber() {
    return Math.floor(Math.random() * 10) + 1; // It will return a value of integar between number 1 to 10 randomly
}
console.log(generateRandomNumber());
// Task 102: Calculating absolute difference between the numbers
function absoluteDifference() {
    return Math.abs(-5 - 5); // It will return the absolute value ignoring the sign
}
console.log(absoluteDifference());
